package com.proativo.util.vo;

public class TipoNrc {
	private Integer valor;
	private Integer tipoNrc;
	private Integer openItemId;
	
	public Integer getValor() {
		return valor;
	}
	public void setValor(Integer valor) {
		this.valor = valor;
	}
	public Integer getTipoNrc() {
		return tipoNrc;
	}
	public void setTipoNrc(Integer tipoNrc) {
		this.tipoNrc = tipoNrc;
	}
	public Integer getOpenItemId() {
		return openItemId;
	}
	public void setOpenItemId(Integer openItemId) {
		this.openItemId = openItemId;
	}
}
